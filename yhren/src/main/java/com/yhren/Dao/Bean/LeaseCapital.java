package com.yhren.Dao.Bean;

public class LeaseCapital {
    private Integer capId;

    private Integer capCount;

    private Double capPrice;

    private Integer capMat;

    public Integer getCapId() {
        return capId;
    }

    public void setCapId(Integer capId) {
        this.capId = capId;
    }

    public Integer getCapCount() {
        return capCount;
    }

    public void setCapCount(Integer capCount) {
        this.capCount = capCount;
    }

    public Double getCapPrice() {
        return capPrice;
    }

    public void setCapPrice(Double capPrice) {
        this.capPrice = capPrice;
    }

    public Integer getCapMat() {
        return capMat;
    }

    public void setCapMat(Integer capMat) {
        this.capMat = capMat;
    }
}